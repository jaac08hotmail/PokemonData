package com.jarroyo.pokemondata.Interactor.Model;

public class AbilityModel{
    private String name;
    private String url;

    public AbilityModel() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
